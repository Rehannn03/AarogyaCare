function useChat() {
  const [messages, setMessages] = useState([]);
  const [input, setInput] = useState("");
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState(null);

  const handleInputChange = (e) => {
    setInput(e.target.value);
  };

  const handleSubmit = async (e) => {
    e.preventDefault();
    if (!input.trim()) return;

    const newMessage = { id: Math.random().toString(36).slice(2), role: "user", content: input };
    setMessages((prev) => [...prev, newMessage]);
    setInput("");
    setIsLoading(true);
    setError(null);

    try {
      const response = await fetch("https://25df-2409-40c0-6c-4cf9-75ed-6ff0-4d6f-b524.ngrok-free.app/query", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ question: newMessage.content }),
      });

      if (!response) {
        throw new Error(`Failed to get a response from the server: ${response.statusText}`);
      }

      const responseData = await response.json();

      if (!responseData) {
        throw new Error("Invalid response format from the server");
      }

      setMessages((prev) => [
        ...prev,
        { id: Math.random().toString(36).slice(2), role: "assistant", content: responseData.response },
      ]);
    } catch (err) {
      setError("Error connecting to the chatbot. Please try again.");
      console.error("Chatbot Error:", err);
    } finally {
      setIsLoading(false);
    }
  };

  return { messages, input, handleInputChange, handleSubmit, isLoading, error };
}




